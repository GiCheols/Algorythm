#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <memory.h>

#define MAX_WORD_SIZE 100
#define MAX_MEANING_SIZE 200

//데이터 형식
typedef struct {
	char word[MAX_WORD_SIZE]; //key field
	char meaning[MAX_MEANING_SIZE];
}element;

//노드의 구조
typedef struct TreeNode {
	element key;
	struct TreeNode* left, * right;
}TreeNode;

/*
만약 e1 < e2 라면, -1 반환
만약 e1 == e2 라면, 0 반환
만약 e1 > e2 라면, 1 반환
*/
int compare(element e1, element e2) { return strcmp(e1.word, e2.word); }

//이진 탐색 트리 출력함수
void display(TreeNode* p) {
	if (p != NULL) {
		printf("(");
		display(p->left);
		printf("%s : %s", p->key.word, p->key.meaning);
		display(p->right);
		printf(")");
	}
}

//이진 탐색 트리 탐색 함수
TreeNode* search(TreeNode* root, element key) {
	TreeNode* p = root;
	while (p != NULL) {
		if (compare(key, p->key) == 0)
			return p;
		else if (compare(key, p->key) < 0)
			p = p->left;
		else if (compare(key, p->key) > 0)
			p = p->right;
	}
	return p; //탐색 실패했을 경우 NULL 반환
}
TreeNode* newNode(element item) {
	TreeNode* tmp = (TreeNode*)malloc(sizeof(TreeNode));
	tmp->key = item;
	tmp->left = tmp->right = NULL;
	return tmp;
}
TreeNode* insertNode(TreeNode* node, element key) {
	//트리가 공백이라면 새로운 노드 반환
	if (node == NULL) return newNode(key);

	//그렇지 않으면 순환적으로 트리를 내려간다.
	if (compare(key, node->key) < 0)
		node->left = insertNode(node->left, key);
	else if (compare(key, node->key) > 0)
		node->right = insertNode(node->right, key);
	//루트 포인터를 반환
	return node;
}
TreeNode* minValueNode(TreeNode* node) {
	TreeNode* current = node;
	//맨 왼쪽 단말 노드를 찾으러 내려감
	while (current->left != NULL)
		current = current->left;
	return current;
}

//이진탐색트리와 키가 주어지면 키가 저장된 노드를 삭제하고 새로운 루트노드를 반환한다.
TreeNode* deleteNode(TreeNode* root, element key) {
	if (root == NULL) return root;

	//만약 키가 루트보다 작으면 왼쪽 서브트리에 있는 것임
	if (compare(key, root->key) < 0)
		root->left = deleteNode(root->left, key);
	//만약 키가 루트보다 크다면 오른쪽 서브트리에 있는 것임
	else if (compare(key, root->key) > 0)
		root->right = deleteNode(root->right, key);
	//키가 루트와 같으면 이 노드를 삭제하면 됨
	else {
		//첫 번째나 두 번째 경우: 자식노드가 없거나 자식 노드가 하나만 있는 경우
		if (root->left == NULL) {
			TreeNode* tmp = root->right;
			free(root);
			return tmp;
		}
		else if (root->right == NULL) {
			TreeNode* tmp = root->left;
			free(root);
			return tmp;
		}
		//세 번째의 경우: 자식노드가 두 개 있는 경우
		TreeNode* tmp = minValueNode(root->right);

		//중외 순회시 후계노드를 복사
		root->key = tmp->key;
		//중외 순회시 후계노드를 삭제
		root->right = deleteNode(root->right, tmp->key);
	}
	return root;
}

void help() {
	printf("\n**** i: 입력, d: 삭제, s: 탐색, p: 출력, q: 종료 ****: ");
}


//이진 탐색 트리를 사용하는 영어 사전 프로그램
int main() {
	char command;
	element e;
	TreeNode* root = NULL;
	TreeNode* tmp;

	do {
		help();
		command = getchar();
		getchar(); //엔터키 제거
		switch (command) {
		case 'i':
			printf("단어: ");
			gets_s(e.word, MAX_WORD_SIZE);
			printf("의미: ");
			gets_s(e.meaning, MAX_MEANING_SIZE);
			root = insertNode(root, e);
			break;
		case 'd':
			printf("단어: ");
			gets_s(e.word, MAX_WORD_SIZE);
			root = deleteNode(root, e);
			break;
		case 'p':
			display(root);
			printf("\n");
			break;
		case 's':
			printf("단어: ");
			gets_s(e.word, MAX_WORD_SIZE);
			tmp = search(root, e);
			if (tmp != NULL)
				//printf("의미: %s\n", e.meaning);
				printf("의미: %s\n", tmp->key.meaning);
			else
				printf("%s is not found!\n", e.word);
			break;
		}
	} while (command != 'q');
	return 0;
}